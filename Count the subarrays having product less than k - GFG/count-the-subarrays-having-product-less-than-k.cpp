class Solution{
  public:
    int countSubArrayProductLessThanK(const vector<int>& a, int n, long long k) {
        int l = 0;
        int r = 0;
        long long temp = 1;
        int ans = 0;
        while(r < n){
            temp *= a[r++];
            while(l < r && temp >= k){
                temp /= a[l++];
            }
            ans += (r - l);
        }
        return ans;
    }
};

